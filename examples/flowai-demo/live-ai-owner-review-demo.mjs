import { writeFileSync } from "node:fs";
import {
  createOpenAiResponsesProvider,
  loadOpenAiProviderConfig,
  orchestrateAiBuilderTurn
} from "../../packages/ai-builder-orchestrator/dist/index.js";
import { generateWorkflowDraft } from "../../packages/workflow-generator/dist/index.js";
import { validateWorkflow } from "../../packages/workflow-dsl/dist/index.js";

const outputPath = "docs/demo/FLOWAI_LIVE_AI_OWNER_REVIEW.md";
const shouldWrite = process.argv.includes("--write");

const source = {
  filename: "owner-review-clinic.md",
  mimeType: "text/markdown",
  content: [
    "# Noor Clinic",
    "Category: clinic",
    "Goal: book appointments and answer common questions.",
    "",
    "## Services",
    "- Dental checkup: Routine dental examination.",
    "- Teeth whitening: Cosmetic whitening consultation.",
    "",
    "## Required fields",
    "- name",
    "- phone",
    "- preferred date",
    "",
    "## FAQs",
    "Q: Do you provide medical diagnosis in chat?",
    "A: No. Medical questions should be routed to clinic staff.",
    "",
    "## Policies",
    "- Cancellation requests should be made 24 hours before the appointment."
  ].join("\n")
};

const deterministic = await orchestrateAiBuilderTurn({
  mode: "disabled",
  source
});
const config = loadOpenAiProviderConfig({
  allowLocalConfig: true,
  workspaceRoot: process.cwd()
});
const live = config.configured
  ? await orchestrateAiBuilderTurn({
      mode: "live_provider",
      source,
      provider: createOpenAiResponsesProvider({
        apiKey: config.apiKey,
        model: config.model
      })
    })
  : null;

const deterministicWorkflow = generateWorkflowDraft({
  businessUnderstanding: deterministic.businessUnderstanding,
  templateHint: "clinic_booking",
  strict: false,
  targetChannel: "telegram_preview"
});
const liveWorkflow = live ? deterministicWorkflow : null;

const markdown = [
  "# FlowAI Live AI Owner Review",
  "",
  "Generated by `pnpm demo:flowai:live`.",
  "",
  "## What The Owner Can See Now",
  "",
  "- Paste or provide business text.",
  "- Review SourceDocument-derived business facts.",
  "- Compare deterministic extraction with live AI review.",
  "- Keep Workflow JSON generation deterministic and validator-backed.",
  "- Preview the same workflow through runtime and mock channels in Studio.",
  "",
  "## Provider Status",
  "",
  codeBlock({
    configured: config.diagnostics.configured,
    source: config.diagnostics.source,
    model: config.diagnostics.model
  }),
  "",
  "## Input",
  "",
  codeBlock({
    filename: source.filename,
    mimeType: source.mimeType,
    contentPreview: source.content
  }),
  "",
  "## Deterministic Result",
  "",
  codeBlock(summarizeResult(deterministic, deterministicWorkflow)),
  "",
  "## Live AI Review Result",
  "",
  live
    ? codeBlock(summarizeResult(live, liveWorkflow))
    : "Live AI was not configured. Deterministic fallback remains available.",
  "",
  "## What Is Deterministic Today",
  "",
  "- Source text ingestion and safety warnings.",
  "- Baseline BusinessUnderstanding extraction.",
  "- WorkflowGenerationPlan selection.",
  "- WorkflowDefinition generation.",
  "- Workflow validation.",
  "- Runtime test conversation.",
  "- Telegram/WhatsApp/web mock previews.",
  "- Export/mapping artifacts.",
  "",
  "## What AI Improves Today",
  "",
  "- Business summary quality.",
  "- Missing question quality.",
  "- Arabic/source-language understanding potential.",
  "- Product/service catalog drafting when source-backed.",
  "",
  "## Still Not Implemented",
  "",
  "- Upload endpoints.",
  "- PDF/DOCX parsing.",
  "- Website crawling.",
  "- RAG/vector database.",
  "- Persistence.",
  "- Auth/tenants/billing.",
  "- Live Telegram or WhatsApp.",
  "- Production deployment.",
  "",
  "## Recommended Decision",
  "",
  "Continue with Studio UI polish for owner review before adding persistence or upload infrastructure."
].join("\n");

if (shouldWrite) {
  writeFileSync(outputPath, markdown, "utf8");
  console.log(`Wrote ${outputPath}`);
} else {
  console.log(markdown);
}

function summarizeResult(result, workflowResult) {
  const workflow = workflowResult?.workflow ?? null;
  return {
    mode: result.mode,
    providerDiagnostics: result.providerDiagnostics,
    businessUnderstanding: {
      businessName: result.businessUnderstanding.businessName,
      category: result.businessUnderstanding.category,
      summary: result.businessUnderstanding.summary,
      services: result.businessUnderstanding.services.map((service) => ({
        name: service.name,
        sourceRefs: service.sourceRefs
      })),
      missingQuestions: result.businessUnderstanding.missingQuestions.map((question) => ({
        id: question.id,
        category: question.category,
        blocksWorkflow: question.blocksWorkflow
      }))
    },
    workflow: workflow
      ? {
          id: workflow.workflowId,
          name: workflow.name,
          valid: validateWorkflow(workflow).valid,
          nodes: workflow.nodes.map((node) => `${node.id}:${node.type}`)
        }
      : null,
    workflowWarnings: workflowResult?.generationPlan.warnings.map((warning) => warning.message) ?? [],
    safetyFindings: result.safetyFindings
  };
}

function codeBlock(value) {
  return ["```json", JSON.stringify(value, null, 2), "```"].join("\n");
}
